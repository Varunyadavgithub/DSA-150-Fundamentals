/* Hint: A Lychrel number is a natural number that never forms a palindrome through the "reverse and add" process. 
   Keep reversing and adding until you reach a palindrome or a predefined iteration limit.
*/

// Example 1:
//      Input: `n = 56`  
//      Output: `Not a Lychrel Number`  
//      Explanation:
//          56 + 65 = 121 (Palindrome in 1 step)

// Example 2:
//      Input: `n = 196`  
//      Output: `Lychrel Number (Suspected)`  
//      Explanation:
//          196 + 691 = 887  
//          887 + 788 = 1675  
//          1675 + 5761 = 7436  
//          ... (Millions of iterations but no palindrome)  

public class LychrelNumberChecker {
    public static boolean isPalindrome(String str) {
        return str.equals(new StringBuilder(str).reverse().toString());
    }

    public static BigInteger reverseNumber(BigInteger num) {
        String reversedStr = new StringBuilder(num.toString(0)).reverse().toString();
        return new BigInteger(reversedStr);
    }

    public static boolean isLychrel(int num) {
        BigInteger number = BigInteger.valueOf(num);
        int maxIterations = 59; // Limit to prevent infinite loops
        for (int i = 0; i < maxIterations; i++) {
            number = number.add(reverseNumber(number));
            if (isPalindrome(number.toString())) {
                return false; // It's NOT a Lychrel number (formed a palindrome)
            }
        }
        return true;
    }

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter a number: ");
        int num = sc.nextInt();
        if (isLychrel(num)) {
            System.out.println(num + " is a Lychrel number.");
        } else {
            System.out.println(num + " is NOT a Lychrel number.");
        }
        sc.close();
    }
}

